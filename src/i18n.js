let activeLanguage = DEFAULT_LANGUAGE;

function normalizeLanguage(value) {
  const normalized = String(value ?? "").trim().toLowerCase().replaceAll("_", "-");
  const base = normalized.split("-")[0];
  return SUPPORTED_LANGUAGES.has(base) ? base : DEFAULT_LANGUAGE;
}

function setLanguage(value) {
  activeLanguage = normalizeLanguage(value);
  return activeLanguage;
}

function getLanguage() {
  return activeLanguage;
}

function t(key, fallback = key) {
  return MESSAGES[activeLanguage]?.[key]
    ?? MESSAGES[DEFAULT_LANGUAGE]?.[key]
    ?? fallback;
}

function localizeText(value) {
  const text = String(value ?? "").trim();
  const key = LEGACY_KEYS.get(text);
  return key ? t(key, text) : text;
}

function feedbackLabel(value) {
  const text = String(value ?? "").trim();
  const key = FEEDBACK_LEGACY_KEYS.get(text);
  return key ? t(key, text) : localizeText(text);
}

function activityLabel(activity) {
  const kind = typeof activity === "string" ? "idle" : String(activity?.kind ?? "idle");
  const code = typeof activity === "object" ? activity?.code : null;
  if (code) return t(code, t(`activity.${kind}`, "Ready"));
  const raw = String(typeof activity === "string" ? activity : activity?.label ?? "").trim();
  if (raw) return localizeText(raw);
  return t(`activity.${kind}`, t("activity.idle", "Ready"));
}

function makeActivity(kind, code) {
  return Object.freeze({ kind, code });
}

